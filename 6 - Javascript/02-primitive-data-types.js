// Primitive Data Types
// String: textual data
// enclosed in '' - single quotation marks
// enclosed in "" - double quotation marks
let firstName = "Jose";
console.log("First name:", firstName);
let anotherName = 'Joseph';
console.log("Another name: ", anotherName);

// typeof operator - determines the types of variable
// operand: variable or expression of the type you want to check
// Syntax: typeof operand
console.log("Type of firstName", typeof firstName);
console.log("Type of anotherName: ", typeof anotherName);

// Number: represents a numeric value
// Javascript one data type is to numeric values unlike other programming language
let age = 30;
console.log("Age: ", age);
console.log("Type of age: ", typeof age);

// Boolean: either true or false
let isStudent = false;
console.log("Is student: ", isStudent);
console.log("Type of isStudent: ", typeof isStudent);

// Null: Intentional absence of value
let intentional = null;
console.log("Intentional: ", intentional);
console.log("Type of Intentional: ", typeof intentional);

// Undefined: Unintentional absense of value
let city = undefined;
console.log("City: ", city);
console.log("Type of City: ", typeof city);